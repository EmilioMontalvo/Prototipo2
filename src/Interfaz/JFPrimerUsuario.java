    public void iniciarActivo(){
        Conexion conn=new Conexion();
        PreparedStatement ps;
        //ResultSet rs;
        try{
            ps=conn.getCon().prepareStatement("INSERT INTO activo values(1,'Sistema')");
            
                                
            ps.executeUpdate();
            //JOptionPane.showMessageDialog(null, "Empleado registrado con éxito");
        }catch(SQLException e){
           if(Pattern.matches("^Duplicate entry.*", e.getMessage())){
                JOptionPane.showMessageDialog(null, "Ya existe un empleado con este número de cédula");
            }else{
                JOptionPane.showMessageDialog(null, "Error de conexión:" + e.getMessage());
           
            }
            
        }finally{
            try{
                if(conn!=null){
                    conn.getCon().close();
                }
            }catch(Exception e){
               JOptionPane.showMessageDialog(null, "Error, intentelo de nuevo"); 
               
            }
        }
        
    }
