    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String parametro = cbxParam3.getSelectedItem().toString();   
        int resp =0;
        int valor=0;
        try{
            if(parametro.equals("")){
                JOptionPane.showMessageDialog(null, "Par叩metro no v叩lido - repita", "ERROR!", 0);
                cbxParam3.setSelectedIndex(-1);
                txtValor3.setText("");
            }else{
                if(txtValor3.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "Valor no v叩lido - repita", "ERROR!", 0);
                    txtValor3.setText("");
                    cbxParam3.setSelectedIndex(-1);
                }else{
                    valor= Integer.parseInt(txtValor3.getText());
                    if( valor<0 || valor>100){
                        if(parametro=="IVA"){
                            JOptionPane.showMessageDialog(null, "Valor de IVA no v叩lido - repita", "ERROR!", 0);
                            cbxParam3.setSelectedIndex(-1); 
                            txtValor3.setText("");
                        }else{
                            JOptionPane.showMessageDialog(null, "Valor de descuento no v叩lido - repita", "ERROR!", 0);
                            cbxParam3.setSelectedIndex(-1); 
                            txtValor3.setText("");
                        }
                    }else{
                        if(parametro=="IVA"){
                            resp = JOptionPane.showConfirmDialog(null, "多Est叩 seguro de actualizar el valor del IVA?", "Actualizar",JOptionPane.YES_NO_OPTION); 
                        }else{
                            resp = JOptionPane.showConfirmDialog(null, "多Est叩 seguro de actualizar el valor de descuento por cumplea単os?", "Actualizar",JOptionPane.YES_NO_OPTION);
                        }
                        
                        if(resp == 0){
                            String sql = "update variables set Valor='"+valor+"' where nombre='"+parametro+"'";
                            conet = con6.getConnection();
                            st = conet.createStatement();
                            st.executeUpdate(sql);
                            cbxEmp3.setSelectedIndex(0);
                            
                            if(parametro=="IVA"){
                                JOptionPane.showMessageDialog(null, "Valor de IVA actualizado exitosamente");
                                cbxParam3.setSelectedIndex(-1); 
                                txtValor3.setText("");
                            }else{
                                JOptionPane.showMessageDialog(null, "Valor de descuento actualizado exitosamente");
                                cbxParam3.setSelectedIndex(-1); 
                                txtValor3.setText("");
                            }

                        }else{
                            
                        } 
                    }
                }
            }

        }catch(Exception e){
            
        }
    }//GEN-LAST:event_jButton5ActionPerformed

